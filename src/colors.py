# ANSI color codes – now in green/black dominance
GREEN  = '\033[92m'      # Bright green (primary)
DARK_GREEN = '\033[32m'  # Standard green
LIME   = '\033[38;5;154m' # Lime green
MINT   = '\033[38;5;121m' # Mint green
FOREST = '\033[38;5;22m'  # Dark green
YELLOW = '\033[93m'       # Keep yellow for warnings (optional)
RED    = '\033[91m'       # Keep red for errors
BLUE   = DARK_GREEN       # Replace blue with dark green
PURPLE = LIME             # Replace purple with lime
CYAN   = MINT             # Replace cyan with mint
NC     = '\033[0m'        # No color (reset)
